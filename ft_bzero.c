#include <stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	x;
	char	*s_tmp;

	x = 0;
	s_tmp = (char*)s;
	while (x < n)
	{
		s_tmp[x] = 0;
		x++;
	}
}
